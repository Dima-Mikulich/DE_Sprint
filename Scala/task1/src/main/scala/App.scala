object App {
  def main(args: Array[String]): Unit = {

    val startString = "Hello, Scala!"
    println(startString)
    println("<--------------->")
    println(startString.reverse)
    println("<--------------->")
    println(startString.toLowerCase)
    println("<--------------->")
    println(startString.replace("!", ""))
    println("<--------------->")
    println(startString + " and goodbye python!")
  }

}
